/**
 * Represents the token type
 */
public enum TokenType {
    /**
     * End of file.
     */
    EOF,
    /**
     * Some word.
     */
    WORD,
    /**
     * Number that fits in long.
     */
    NUMBER,
    /**
     * Some symbol.
     */
    SYMBOL
}
